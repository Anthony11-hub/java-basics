package com.company;

public class Loops {
    public static void main(String[] args){
        //for loops === We can decrement or increment this
        for (int i = 0;i<=5;i++)
            System.out.println("Number "+i);
        //while loops
        int t = 0;
        while(t<=5){
            System.out.println("Number "+t);
            t++;
        }

    }
}
